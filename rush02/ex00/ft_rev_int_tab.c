void	ft_swap(int *a, int *b)
{
	int	c;

	c = *a;
	*a = *b;
	*b = c;
}

void	ft_rev_int_tab(int *tab, int size)
{
	int	i;

	i = 0;
	if (size < 2)
		return ;
	while (i < --size)
		ft_swap(&tab[i++], &tab[size]);
}
